function country_selected() {
    "use strict";
    var c = $('#myselect').val();

    for (var a in portugal) {
        $("#4").append('<option value="' + a + '">' + a +'</option>');
    }

    console.log(c);
    if (c === 'Portugal') {
        $('#3').show();
        $('#4').show();
    }if (c !== 'Portugal') {
        $('#3').hide();
        $('#4').hide();
        $('#5').hide();
        $('#6').hide();
        $("#4").empty().append('<option value="Other" selected>Other...</option>');
        $("#6").empty().append('<option value="Other" selected>Other...</option>');
    }
}

function city_selected() {
    var s = $("#4").val();

    for (var a in portugal[s]) {
        $("#6").append('<option value="' + portugal[s][a] + '">' + portugal[s][a] +'</option>');
    }

    $('#5').show();
    $('#6').show();
}

